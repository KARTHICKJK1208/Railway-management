    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        try {
            // SQL command data stored in String datatype
            String sql = "select * from AVAILABILITY";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                String tno = rs.getString("TRAIN_NO");
                String cc = rs.getString("REM_CC");
                String sl = rs.getString("REM_SL");
                String a3 = rs.getString("REM_3A");
                String a2 = String.valueOf(rs.getString("REM_2A"));
                String a1 = String.valueOf(rs.getString("REM_1A"));
                
                Object[] row = { tno,cc,sl,a3,a2,a1};
                DefaultTableModel model = (DefaultTableModel) tab.getModel();
                model.addRow(row);
            }
        }

        catch (SQLException e) {
            Logger.getLogger(passenger.class.getName()).log(Level.SEVERE, null, e);
        JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton4ActionPerformed

