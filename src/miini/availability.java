    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
try{
           String sql1 = "select * from AVAILABILITY where TRAIN_NO = '"+TRAIN_NO.getText()+"'";
            st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Train Details Already Exists");
            }
            else{
                if(TRAIN_NO.getText().equals("") || REM_CC.getText().equals("") ||  REM_SL.getText().equals("") || REM_3A.getText().equals("")  || REM_2A.getText().equals("") || REM_1A.getText().equals("")){
                        JOptionPane.showMessageDialog(this,"Invalid Input!");
                }
                else{
                    try{
                        String sql ="insert into AVAILABILITY values(?,?,?,?,?,?)";
                        ps=con.prepareStatement(sql);
                        ps.setInt(1,Integer.parseInt(TRAIN_NO.getText()));
                        ps.setInt(2,Integer.parseInt(REM_CC.getText()));
                        ps.setInt(3,Integer.parseInt(REM_SL.getText()));
                        ps.setInt(4,Integer.parseInt(REM_3A.getText()));
                        ps.setInt(5,Integer.parseInt(REM_2A.getText()));
                        ps.setInt(6,Integer.parseInt(REM_1A.getText()));
                        ps.execute();
                        JOptionPane.showMessageDialog(this,"Train Details Inserted");
                    }
                    catch(SQLException ex){
                        Logger.getLogger(availability.class.getName()).log(Level.SEVERE,null, ex);
                        JOptionPane.showMessageDialog(this,ex.getMessage());
                    }
                    
                }
                
            }

        }
        catch(Exception ex){
           Logger.getLogger(passenger.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(this, ex.getMessage());
 
        }                       // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

