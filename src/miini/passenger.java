    private void INSERTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_INSERTActionPerformed
        // TODO add your handling code here:
        try{
           String sql1 = "select * from PASSENGER where P_ID = '"+P_Id.getText()+"'";
            st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
            rs = st.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog(this, "Passenger ID Already Exists");
            }
            else{
                if(P_Id.getText().equals("") || F_Name.getText().equals("") ||  L_Name.getText().equals("") || Age.getText().equals("")  || Gender.getText().equals("") || Phone.getText().equals("")){
                        JOptionPane.showMessageDialog(this,"Invalid Input!");
                }
                else{
                    try{
                        String sql ="insert into PASSENGER values(?,?,?,?,?,?)";
                        ps=con.prepareStatement(sql);
                        ps.setString(1,P_Id.getText());
                        ps.setString(2,F_Name.getText());
                        ps.setString(3,L_Name.getText());
                        ps.setInt(6,Integer.parseInt(Age.getText()));
                        ps.setString(4,Gender.getText());
                        ps.setString(5,Phone.getText());
                        ps.execute();
                        JOptionPane.showMessageDialog(this,"Passenger Inserted");
                    }
                    catch(SQLException ex){
                        Logger.getLogger(passenger.class.getName()).log(Level.SEVERE,null, ex);
                        JOptionPane.showMessageDialog(this,ex.getMessage());
                    }
                    
                }
                
            }

        }
        catch(Exception ex){
           Logger.getLogger(passenger.class.getName()).log(Level.SEVERE, null, ex);
           JOptionPane.showMessageDialog(this, ex.getMessage());
 
        }
    }//GEN-LAST:event_INSERTActionPerformed

