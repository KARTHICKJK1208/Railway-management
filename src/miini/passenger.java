    private void DISPLAYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DISPLAYActionPerformed
        // TODO add your handling code here:
        try {
            // SQL command data stored in String datatype
            String sql = "select * from PASSENGER";
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();
            while (rs.next()) {
                String id = rs.getString("P_ID");
                String fname = rs.getString("F_NAME");
                String lname = rs.getString("L_NAME");
                String gen = rs.getString("Gender");
                String phone = String.valueOf(rs.getString("Phone"));
                String age = String.valueOf(rs.getString("Age"));
                System.out.println(id + "\t\t" + fname
                                   + "\t\t" + lname);
                
                Object[] row = { id,fname,lname,gen,phone,age};
                DefaultTableModel model = (DefaultTableModel) tab.getModel();
                model.addRow(row);
            }
        }

        catch (SQLException e) {
            Logger.getLogger(passenger.class.getName()).log(Level.SEVERE, null, e);
        JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_DISPLAYActionPerformed

